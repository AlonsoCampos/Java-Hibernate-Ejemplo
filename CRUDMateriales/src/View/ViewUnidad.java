void limpiarTabla()
    {
        while (unidadjTable.getRowCount()!=0)
        {
         ((DefaultTableModel)unidadjTable.getModel()).removeRow(0);
        }
    }
void ChoiseUnidad(){
        if(unidadjTable.getSelectedRow()>=0){
            confirmarjPanel.setVisible(true);
        }else{
            confirmarjPanel.setVisible(false);
        }
    }
public static Unidades CodigoUnidad(int ubicacion){
        Unidades idUnidad= Controller.ConsultasMateriales.UnicaUnidad(ubicacion);
        if(idUnidad!=null){
            return idUnidad;
        }else{
            return null;
        }
    }
